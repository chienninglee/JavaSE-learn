package com.itheima.polymorphism.oopstatic.test;

public class Test12 {
    //打印1-5；打印5-1
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }
}
