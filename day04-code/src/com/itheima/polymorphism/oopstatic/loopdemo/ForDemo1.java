package com.itheima.polymorphism.oopstatic.loopdemo;
//打印5次 java

/*for循环：
        for(初始化语句;条件判断语句;条件控制语句){
        循环控制语句;
        }*/
public class ForDemo1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i + ":" + "Java");
        }
    }
}
