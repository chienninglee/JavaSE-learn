package com.itheima.compareoperator;
//比较运算符
public class CompareOperatorDemo1 {
    public static void main(String[] args) {
        // ==:判断ab是否相等。
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);//true
        System.out.println(a == c);//false

    }
}
